const DAY_NAMES = {
  en: ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"],
  es: ["Domingo", "Lunes", "Martes", "Miércoles", "Jueves", "Viernes", "Sábado"],
};
const DAY_NAMES_SHORT = {
  en: ["Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"],
  es: ["Dom", "Lun", "Mar", "Mié", "Jue", "Vie", "Sáb"],
};

let lang = localStorage.getItem("msg_admin_lang") || localStorage.getItem("msg_lang") || "en";
if (!dict[lang]) lang = "en";

const listeners = new Set();

export function getAdminLang() {
  return lang;
}

export function adminLocale() {
  return lang === "es" ? "es-ES" : "en-US";
}

export function days() {
  return DAY_NAMES[lang] || DAY_NAMES.en;
}

export function daysShort() {
  return DAY_NAMES_SHORT[lang] || DAY_NAMES_SHORT.en;
}

export function dayName(i) {
  return days()[i] ?? "";
}

export function t(key, fallback) {
  return dict[lang]?.[key] ?? dict.en[key] ?? fallback ?? key;
}

/** Translates static markup: [data-i18n] sets text, [data-i18n-ph] sets placeholder. */
export function applyStaticI18n(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = dict[lang]?.[key] ?? dict.en[key];
    if (val != null) el.textContent = val;
  });
  root.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    const key = el.getAttribute("data-i18n-ph");
    const val = dict[lang]?.[key] ?? dict.en[key];
    if (val != null) el.setAttribute("placeholder", val);
  });
  document.documentElement.lang = lang;
}

export function setAdminLang(next) {
  if (!dict[next]) return;
  lang = next;
  localStorage.setItem("msg_admin_lang", next);
  applyStaticI18n();
  document.getElementById("langEN")?.classList.toggle("is-active", next === "en");
  document.getElementById("langES")?.classList.toggle("is-active", next === "es");
  listeners.forEach((cb) => {
    try { cb(next); } catch { /* ignore listener errors */ }
  });
}

export function onLangChange(cb) {
  listeners.add(cb);
}

/** Wires the EN/ES toggle buttons and applies the current language immediately. */
export function initLangToggle() {
  const en = document.getElementById("langEN");
  const es = document.getElementById("langES");
  en?.addEventListener("click", () => setAdminLang("en"));
  es?.addEventListener("click", () => setAdminLang("es"));
  en?.classList.toggle("is-active", lang === "en");
  es?.classList.toggle("is-active", lang === "es");
  applyStaticI18n();
}
